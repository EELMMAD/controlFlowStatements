package se.udemy.elmira;

public class WhileAndDoWhileStatements {
    public static void main(String[] args) {
        int count = 1;
        while (count != 6) {
            System.out.println("Count value is " + count);
            count++;
        }

        for (int i = 1; i<7; i++){
            System.out.println("Count value is " + count);
        }

    }

}
